package com.mygdx.game.utils;

public class Pos {
    private float x;
    private float y;

    public Pos() {
    }

    public Pos(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }
}
